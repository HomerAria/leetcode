class Solution {
    public void rotate(int[] nums, int k) {
        // Solution 1: multiple reverse
        // Reverse this array by three times.
        k = k % nums.length;
        reverseArray(nums, 0, nums.length - 1);
        reverseArray(nums, 0, k - 1);
        reverseArray(nums, k, nums.length - 1);

    }

    /*
    Only reverse part of array, which starts from 'start' and ends at 'end'
    */
    void reverseArray(int[] nums, int start, int end){
        // Set two pointers, when this two borders meet with each, ends the traversal.
        int left = start;
        int right = end;

        while(left < right){
            int temp = nums[left];
            nums[left] = nums[right];
            nums[right] = temp;
            left++;
            right--;
        }
    }
}
